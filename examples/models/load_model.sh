REDIS_CLI=../../deps/redis/src/redis-cli

echo "SET GRAPH"
$REDIS_CLI -x DL.GRAPH foo < graph.pb

echo "SET TENSORS"
$REDIS_CLI DL.TENSOR a FLOAT 1 2 VALUES 2 3
$REDIS_CLI DL.TENSOR b FLOAT 1 2 VALUES 2 3

echo "GET TENSORS"
$REDIS_CLI DL.VALUES a
$REDIS_CLI DL.VALUES b

echo "RUN GRAPH"
$REDIS_CLI DL.RUN foo 2 a a b b c mul

echo "GET TENSOR"
$REDIS_CLI DL.VALUES c

echo "GET TENSOR RAW"
$REDIS_CLI --raw DL.DATA c

echo "NDIMS" `$REDIS_CLI DL.NDIMS c`

echo "DIMS" `$REDIS_CLI DL.DIMS c`

echo "TYPE" `$REDIS_CLI DL.TYPE c`

echo "SIZE" `$REDIS_CLI DL.SIZE c`

